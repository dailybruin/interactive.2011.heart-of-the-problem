			<h3 class="subhead">Thi&ecirc;n B&igrave;nh Orphanage and SOS Children&rsquo;s Village share strong passion for soccer
</h3>
<p>The shirtless 17-year-old, engulfed by a mess of bodies and flailing limbs, jabbed his bare foot at a soccer ball pinned between bamboo stalks.</p>
<p>Lanh reached down, grabbed the ball with his hands and booted it upfield with a grin.</p>
<p>Game on.</p>
<p>Players were joining by the minute, kicking off their shoes and socks to run barefoot through the field, a hazardous swath of dirt turned to mud by the rain and pocked with rocks and puddles.</p>
<p>This is the long-awaited rematch between the children of Thi&ecirc;n B&igrave;nh Orphanage and the students of Medical, Educational Missions and Outreach. The rivalry has racked up numerous wins for the children and zero for the college students.</p>
<p>For years, MEMO has supported Thi&ecirc;n B&igrave;nh&rsquo;s operations, providing funding for health care, school supplies and clothing. Every year, MEMO students bring toys and chocolates to the children.</p>
<p>Perhaps most importantly, MEMO brings soccer.</p>
<p>&ldquo;I&rsquo;m not going to lie, we try our best,&rdquo; said Tuan Nguyen, a third-year physiological sciences student, smiling. &ldquo;Throughout the whole trip, I&rsquo;m like, &lsquo;We&rsquo;re going to beat these kids, guys.&rsquo;&rdquo;</p>
<p>Despite their best efforts to practice and prepare this year, MEMO still didn&rsquo;t stand a chance. The orphans dominated possession for the majority of the game, running circles around MEMO. When it was finally time for the college students to leave, the score was 6-2 in favor of the orphans, and the group was soaked and covered in mud. They exchanged high-fives and laughs as they reconvened under a tree for a group photo.</p>
<p>Days later and hundreds of miles north, MEMO visited SOS Children&rsquo;s Village near the city of Hue, a private orphanage founded by an Austrian philanthropist. The group had never been here before but, on the field, that didn&rsquo;t seem to slow anyone down.</p>
<p>A flood of children migrated from the front of the village to a large, grassy field in the middle of the complex. Net-less metal goals stood facing each other some 50 yards away, waiting for play. This was a proper soccer field.</p>
<p>The SOS Village orphans took to one side of the field and the MEMO students the other.</p>
<p>Kids connected passes and made runs up-field. When someone called a hand ball, three boys instinctively formed a wall to defend the indirect penalty kick.</p>
<p>These kids didn&rsquo;t just play pick-up games&mdash;the orphans were ranked third in the nation in their league.</p>
<p>Another 6-0 loss under their belt, the college students still left the field with smiles and high-fives.</p>
<p>Soccer is the one constant between these two disparate orphanages. SOS Village often turns out educated members of society, while Thi&ecirc;n B&igrave;nh&rsquo;s orphans tend to stay at the bottom.</p>
<p>Even though appearances suggest it is more well off than Thi&ecirc;n B&igrave;nh, SOS Children&rsquo;s Villages International still serves some of Vietnam&rsquo;s poorest in Quang B&igrave;nh province.</p>
<p>Each child is sponsored for about $30 a month, and with that money they go to school outside the village and reintegrate more easily with society, caretaker Hien Nguyen said.</p>
<p>Many of the orphans even earn scholarships to attend universities.</p>
<p>The kids live in family-style dwellings named after flowers.</p>
<p>In the &ldquo;Forget-Me-Not&rdquo; House, Nguyen looks after 10 children and helps tend their own fruit and vegetable garden.</p>
<p>Thi&ecirc;n B&igrave;nh orphanage is not so fortunate, despite getting some external assistance from groups such as MEMO.</p>
<p>Managed by a group of nuns, the orphanage houses about 90 children, in addition to the elderly and people with mental disabilities.</p>
<p>They live in a meandering complex of concrete buildings, which do little to protect from the humidity. The orphans&rsquo; rooms are musty and require mosquito nets around the cots.</p>
<p>Many of the kids go on to vocational schools or end up working odd jobs that require little education.</p>
<p>&ldquo;It&rsquo;s an imperfect system in a sense that if you&rsquo;re born in a bad circumstance, you&rsquo;re kind of stuck,&rdquo; said Long-Co Nguyen, co-founder of MEMO and UC San Francisco medical student.</p>
<p>Barefoot and muddied, Lanh and other the orphan soccer stars of Thi&ecirc;n B&igrave;nh gravitated toward Tuan Nguyen, flanking him as he walked back to the bus.</p>
<p>They asked him questions about his life in America and were perhaps most interested in how he got to Vietnam.</p>
<p>&ldquo;Do you take the plane to go there? How far is the flight (to America)? Do you see us from up there? Do we look like ants from up there?&rdquo; the orphans asked.</p>
<p>Nguyen patiently responded, lagging behind the others who had filed back onto the bus.</p>
<p>Up until the moment he stepped aboard, the children followed him. Even as Nguyen sat down, he reached out through the window to shake their hands.</p>
<p>Several orphans, Lanh included, ran after the bus as it drove down the muddy path.</p>
<p>Lanh swung his arms in a wave, yelling goodbyes from a plastic chair parked next to the exit as the bus turned the corner and drove out of sight.</p>
<p>Until next year.</p>
