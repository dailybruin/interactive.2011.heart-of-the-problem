			<h2>In Vietnam, two disparate orphanages aim to achieve a common goal</h2>
									<p style="float:right"><a href="https://twitter.com/share" class="twitter-share-button" data-count="horizontal" data-via="dailybruin">Tweet</a><script type="text/javascript" src="//platform.twitter.com/widgets.js"></script>
