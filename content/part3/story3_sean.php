			<h3 class="subhead">Meaningful communication can happen anywhere, despite a lack of common language</h3>
<p>On the other side of the world, I often found myself at a loss for words.</p>
<p>With no working knowledge of the language, I was at the mercy of my interpreter and the students of Medical, Educational Missions and Outreach to hear the stories of the local Vietnamese. Luckily, the details weren&rsquo;t lost in translation.</p>
<p>But sometimes, I was on my own.</p>
<p>Like the cab ride that took me and my colleague past our destination and halfway across Hanoi. The driver pinned the error on me, and tried to charge us the full fare.</p>
<p>After a mild shouting match in Vietnamese and English outside a row of embassies, we reached a begrudging settlement.</p>
<p>Or, the second day of the trip, when I found myself on the back of a motorbike in rural Long An.</p>
<p>Two MEMO officers and I were going to see the house the service group bought for a family who had been without a proper home for three generations.</p>
<p>I was given but one warning from one of the students.</p>
<p>&ldquo;The path is a bit rough, but just trust the driver.&rdquo;</p>
<p>The driver spoke a few words to me in Vietnamese, then sped off down the dirt road before I even realized I didn&rsquo;t have a helmet.</p>
<p>The path became more of a narrow trail, cut out from the mass of trees and vegetation. When we came upon a creek crossing, the driver nudged me off the bike so I could run across the wood plank bridge.</p>
<p>Afterward, I couldn&rsquo;t even thank the driver, who turned out to be the provincial leader of Long An, when I arrived back at the school alive.</p>
<p>And I won&rsquo;t even begin to describe my adventures trying to haggle with street merchants in the city.</p>
<p>But during a visit to the Thi&ecirc;n B&igrave;nh Orphanage in the outskirts of Ho Chi Minh City, one orphan boy showed me that meaningful communication doesn&rsquo;t necessarily require an exchange of words.</p>
<p>He confidently ran and bounced across the slippery linoleum floor in his bare feet.</p>
<p>He and the other orphans hammed it up and made funny faces for digital cameras, then giggled at the sight of their own image.</p>
<p>He twisted and turned a foam soccer ball in his hands and leaped forward as he heaved the ball toward me. The whole spectacle, combined with his lack of coordination, made catching the ball somewhat difficult, though I could just be making excuses.</p>
<p>Once I caught up to the ball, it was my turn. I imitated his same ritual, turning the ball over in my hands and sending it soaring far over his head.</p>
<p>Despite the overwhelming humidity, he pursued the object with a kind of boundless energy, only to start the game over again.</p>
<p>I didn&rsquo;t have the words to ask him his name or age. I could only guess he was about 6 years old. At that point, I had only learned the Vietnamese words for &ldquo;hello&rdquo; and &ldquo;thank you very much.&rdquo;</p>
<p>But in that moment, words didn&rsquo;t matter. The only communication we needed was eye contact and maybe a shared laugh or two.</p>
<p>It was so simple that probably no one noticed, but for a time, the orphans ceased being orphans. I ceased being a foreigner. That is, until the end of the visit, when one Calvin Klein-clad boy pointed at me and yelled &ldquo;b&aacute;nh b&egrave;o, b&aacute;nh b&egrave;o!&rdquo;&mdash;likely comparing my appearance to a Vietnamese rice pancake topped with brown shrimp flakes.</p>
<p>It&rsquo;s these connections, the kind MEMO students have been making at this orphanage for years, that are perhaps the ones that mean the most.</p>
<p>It brings to mind the quote, often attributed to Maya Angelou, &ldquo;They may forget what you said, but they will never forget how you made them feel.&rdquo;</p>
<p>As MEMO works to make a sustainable difference in Vietnam, years from now, the personal connection MEMO students make with the orphans will last as well.</p>
<p>The children will always remember the annual visit from MEMO, the chocolates, the new toys, the games.</p>
<p>At a loss for words at Thi&ecirc;n B&igrave;nh, a silly game of pass speaks loudest of all.</p>
