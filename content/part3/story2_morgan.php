			<h3 class="subhead">The challenge of photojournalism lies in capturing a moment and all the feelings in it, while letting the viewers feel it for themselves</h3>
<p>I was drawn to the blue tinge of her fingers and toes. The picked bug bites. The zipper-like scar in the center of her sternum.<p>
<p>I noticed something new about Hien Thu Thi Vo with every minute that passed.</p>
<p>The matted hair against her forehead from the stifling heat and humidity, her bloodshot eyes, the yellow fur that stuck to her skin from the stuffed bear she refused to let out of her grasp.</p>
<p>I snapped away furiously. I had hundreds of photos already, but I couldn&rsquo;t stop. She didn&rsquo;t even speak, yet there was something immensely captivating about her. Every forlorn look, every deadpan stare&mdash;somehow it all seemed different each time I pulled my eye away from the viewfinder.</p>
<p>It&rsquo;s funny how, as a photographer, you latch onto a scene and suddenly you&rsquo;re pouring every ounce of effort and energy into sharing it exactly as it was&mdash;exactly as you saw it.</p>
<p>I didn&rsquo;t want someone to look at my photos and see a girl in a hospital. I wanted them to see Hien. I wanted them to know her, know her silence, know what it felt like to sit with her while she and her mother waited for the news that would determine her fate and opportunity for a full life.</p>
<p>So I found myself looking down at the screen time and time again, frustrated. The image was never quite what I saw in front of me. I changed lenses, I changed cameras, I shot from every angle and distance&mdash;and yet I still don&rsquo;t think any collection of photographs can transport you to that humid, crowded hallway, air electrified with anxiety and waning hope.</p>
<p>This is what frustrates me about photojournalism: the inability to fully recreate an experience.</p>
<p>I entered this line of work with the intention of providing an accurate representation of not just the events that took place but the emotions that accompanied them. I didn&rsquo;t want someone to see a photograph, I wanted them to feel it.</p>
<p>But with experience it has become increasingly apparent to me that this isn&rsquo;t entirely possible. I cannot recreate real life. I cannot make the intangible tangible.</p>
<p>So this is what I grapple with. The fact that I will never be able to do my job the way I have idealized it.</p>
<p>But I continue to try.</p>
<p>I&rsquo;ve learned that it is not my job to create emotion, but as the only liaison between the audience and the subject, my attention to detail and conscious decisions as a photographer are crucial in the process.</p>
<p>There is something about a photograph, a snapshot of a single moment in time that is more powerful and telling than any other medium. The blue tinge, the worn pajamas, the tufts of fur that cling to her lips&mdash;these are details that are quickly forgotten, that slip away with time.</p>
<p>A photo lets you linger.</p>
<p>My job is not to create emotion. It is to present an instant in time with all of the details and to let the viewer&rsquo;s emotions and opinions unfold as they may. Because it&rsquo;s not about handing them my thoughts&mdash;it&rsquo;s about provoking theirs.</p>
