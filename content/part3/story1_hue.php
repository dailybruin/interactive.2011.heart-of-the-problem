			<h3 class="subhead">MEMO students make the hard decision to persevere despite the medical clinic being shut down</h3>
<p>HUE, Vietnam&mdash;Small groups of students huddled together, crying.</p>
<p>One woman stormed off with a shriek of frustration.</p>
<p>&ldquo;It&rsquo;s not fair!&rdquo;</p>
<p>The students of Medical, Educational Missions and Outreach had just been given the news that the medical clinics they had planned and looked forward to all year had been shut down by the local government.</p>
<p>The monsoon rain outside was the only sure thing that night in Hue.</p>
<p>Were there people outside monitoring the students in the hotel? If they left, would someone follow them?</p>
<p>All of these paled in comparison to the question of &ldquo;What do we do now?&rdquo;</p>
<p>With the last facet of the mission cancelled, the students had just less than a week left in Vietnam.</p>
<p>Late in the evening, most of the 50 students met in a hotel room to discuss their options</p>
<p>Tuan Ngo, then president of MEMO UC Irvine, laid out the options to the group.</p>
<p>They could cut the trip short and take the first flight home, they could wait it out for the rest of the trip, or they could fly back to Ho Chi Minh City and visit orphanages.</p>
<p>While helping more orphanages would seem like the nobler choice, Long-Co Nguyen, MEMO co-founder, told the others that the group should instead save its money until next year for programs that make a sustainable difference.</p>
<p>&ldquo;We can actually help Vietnam help themselves &hellip; not this dependency thing we&rsquo;re doing now,&rdquo; she said.</p>
<p>Clinics do save people, she said, but &ldquo;at the end of the day it&rsquo;s just a Band-Aid solution and nothing more than that.&rdquo;</p>
<p>Ultimately, the students decided to stay in Vietnam, sightseeing and visiting one more orphanage&mdash;SOS Children&rsquo;s Village in Quang Binh province.</p>
<p>To move beyond this bandage solution, MEMO leaders focus on educating the Vietnamese people so they might push their country forward.</p>
<p>One way the group plans to do that is to introduce the Vietnamese to medical technologies, such as the pulse oximeter.</p>
<p>For about $30 online, this device indirectly measures oxygen levels in the blood and can screen for congenital heart defects, Nguyen said in an interview.</p>
<p>MEMO has plans to teach rural midwives in the Central Highlands how to use the device and lobby for a government policy to use it on all newborns.</p>
<p>Early detection could mean the difference.</p>
<p>On a visit to a private heart hospital, the MEMO students met a young boy whose hands and feet were amputated because of complications that arose in surgery.</p>
<p>&ldquo;Something so simple as this &hellip; we really should be able to get this into practice,&rdquo; said Steven Pham, a fourth-year biochemistry student and last year&rsquo;s MEMO UCLA president. &ldquo;If we can catch these (problems) early, we can avoid these problems later on.&rdquo;</p>
<p>The group also has plans to bring Hepatitis B screening and other self-sustaining programs to Vietnam.</p>
<p>Pham, who started UCLA&rsquo;s MEMO chapter two years ago, said seeing the group members move forward after Hue, especially on their second orphanage visit, reaffirmed his sense of optimism.</p>
<p>&ldquo;They were confused, but they still understood that the people weren&rsquo;t being helped,&rdquo; he said. &ldquo;I think when I saw everyone tearing up, I realized there&rsquo;s a lot of passion still.&rdquo;</p>
<p>This year, MEMO is expanding, adding a chapter at UC San Diego.</p>
<p>MEMO at UCLA also has plans to expand fundraising efforts among San Jose&rsquo;s Vietnamese community, said Amanda Tran, a third-year microbiology, immunology and molecular genetics student and MEMO fundraising chair.</p>
<p>Without a MEMO chapter in Northern California, Tran said some of her ideas would require more coordination with other nonprofits or high school clubs.</p>
<p>Tran said she is not discouraged from losing the clinics. To her, it shows MEMO is still needed, even if there are obstacles.</p>
<p>&ldquo;It&rsquo;s really sad that even if you come with (the) best intentions, it&rsquo;s not always well-received,&rdquo; she said. &rdquo;But we keep trying. &hellip; I think that&rsquo;s the spirit of MEMO.&rdquo;</p>
<p>Many MEMO students agreed with Tran. They&rsquo;re ready to move forward in next year&rsquo;s mission.</p>
<p>Tran Huynh, a third-year UC Irvine student originally from Hue, said she thought MEMO should try to set up one day of clinics in the next mission.</p>
<p>&ldquo;No matter what happens, I still love (Hue) just like I always did,&rdquo; she said. &ldquo;I think we should try again &hellip; to see what we can achieve.&rdquo;</p>
<p>Tuan Nguyen, a third-year physiological sciences student and now co-president of MEMO at UCLA, said the group would have to be more careful about where it takes its clinics next year.</p>
<p>The areas where people need the most help are also the riskiest in terms of volunteering.</p>
<p>&ldquo;It&rsquo;s like shooting an arrow into the dark. &hellip; It&rsquo;s not going to always be successful, but you keep trying and eventually you get through,&rdquo; Nguyen said.</p>
<p>This year, Pham moved to a new position in MEMO&mdash;director of the nonprofit organization, overseeing its college chapters and other projects.</p>
<p>In the days after Hue, he said, he realized his job in Vietnam would never be done.</p>
<p>He recalled a story he was told about starfish caught in a tide pool.</p>
<p>A boy stood on the beach and picked up the creatures – one by one – to throw them back into the ocean. The coast was littered with them.</p>
<p>A man took notice of the boy&rsquo;s efforts.</p>
<p>&ldquo;All of those are going to die.&rdquo;</p>
<p>The boy reached down to throw another starfish into the water.</p>
<p>&ldquo;Well, not this one.&rdquo;</p>
