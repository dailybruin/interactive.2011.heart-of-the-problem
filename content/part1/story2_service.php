			<h3 class="subhead">Denied by local officials, MEMO is unable to carry out its plans to set up clinics in Vietnam</h3>
<p>HUE, Vietnam&mdash;He couldn&rsquo;t hold back tears as he stood before the group.</p>
<p>One man&rsquo;s whim just put an end to almost a year of hard work and planning and Steven Pham had to deliver the news.</p>
<p>The service group&rsquo;s health clinics had been shut down by the local health minister at the last minute.</p>
<p>Pham imagined some of Vietnam&rsquo;s poorest people, waiting the next morning for the clinics to be set up by the UCLA and UC Irvine students of Medical, Educational Missions and Outreach.
He broke down.</p>
<p>&ldquo;Our trip, all the people we tried to help with the ultrasound and stuff, that&rsquo;s not going to happen,&rdquo; the fourth-year biochemistry student said. &ldquo;I&rsquo;m really sorry&madsh;we really tried.&rdquo;</p>
<p>The higher government had given MEMO the green light, to hold the three-day health clinic, but it was the local officials in the country&rsquo;s central province that took objection.</p>
<p>&ldquo;Health care is not just about being able to provide the right medicine, it is about politics, as well,&rdquo; said Long-Co Nguyen, MEMO co-founder and UC San Francisco medical student.</p>
<p>The group was too large and the clinic too small. The college students were not qualified. They weren&rsquo;t all Vietnamese speakers.</p>
<p>MEMO had a counter to each of the excuses, but in the end, a representative of the local government said the clinics just could not happen.</p>
<p>&ldquo;We were just trying to go into the area and do what we do – to help people,&rdquo; Pham said. &ldquo;I don&rsquo;t think we&rsquo;ll ever have the perfect explanation why.&rdquo;</p>
<p>Before this, MEMO had never been stopped from doing a project; but in developing nations such as Vietnam, service groups are denied permits and access to areas for reasons often difficult, if not impossible, to identify.</p>
<p>While hundreds of service groups operate successfully in Vietnam each year, it isn&rsquo;t easy to set up work in the region because of a suspicion of foreign influence, said Thu-Huong Nguyen-Vo, a UCLA associate professor of the South East Asian and Asian American studies.</p>
<p>Hue authorities are especially vigilant when it comes to avoiding potential problems related to foreign service groups, Nguyen-Vo said.</p>
<p>The southern region of Vietnam has increasingly opened its doors to foreign aid, but the central and northern regions remain relatively closed off, Long-Co Nguyen said.</p>
<p>&ldquo;It&rsquo;s maddening, the type of thing (service groups) have to navigate,&rdquo; Nguyen-Vo said.</p>
<p>MEMO leaders said they believe the summer&rsquo;s turbulent political climate in the Middle East put the central government on edge.</p>
<p>Ho Chi Minh City and Hanoi were the sites of nationalist protests against China&rsquo;s claim over a disputed island chain in the South China Sea.</p>
<p>Increased tensions in the country make the government less tolerant of things it deems suspicious, Nguyen-Vo said.</p>
<p>While the political context is important, Nguyen-Vo said money plays just as big a role.</p>
<p>Corruption is an endemic problem, often the bane of those who attempt to volunteer or do business in the country, said George Dutton, a UCLA associate professor of South East Asian studies.</p>
<p>In MEMO&rsquo;s case, the officials offered to accept the group&rsquo;s medications and put on the clinics themselves.</p>
<p>The college students would be able to observe but not directly interact with the patients, the officials told them.</p>
<p>MEMO chose not to go along.</p>
<p>The students decided they would only provide their services directly so they could ensure no donations were misspent, several group members said.</p>
<p>Nguyen-Vo said that offers such as this raise a red flag, adding that there will be individuals within the local government who benefit monetarily from running the clinics.</p>
<p>&ldquo;They have to grease the right hand &hellip; to make things happen,&rdquo; Nguyen-Vo said. &ldquo;That&rsquo;s how you get things done in Vietnam.&rdquo;</p>
