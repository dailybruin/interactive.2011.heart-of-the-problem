			<h3 class="subhead">MEMO helps fund a new home for a Long An province family that has lived in a makeshift shelter for three generations</h3>
<p>LONG AN, Vietnam&mdash;Lost among the fields and rice paddies of Long An stands a one-room brick house with a tiny sign at the top.</p>
<p>&ldquo;A gift from MEMO.&rdquo;</p>
<p>The only way to the house is through a narrow dirt trail, two miles from B&igrave;nh H&ograve;a Nam Elementary.</p>
<p>The road crosses over a wide network of creeks – one bump could easily send a passenger on a bike either soaring into a thicket of plants or plunging into the canal.</p>
<p>Each day, Ngan Kim Nguyen takes this road home from school on a brown loaner bicycle.</p>
<p>Not too long ago, however, the seventh grader didn&rsquo;t have much of a home to return to.</p>
<p>The family had been without a real house for three generations, living instead in a shelter created from bamboo and straw.</p>
<p>Earlier in the year, MEMO&rsquo;s contacts came to the group with an opportunity to help.</p>
<p>Selling leftover medicine they had in stock, the group produced 14 million Vietnamese dong, about $700, to go toward building a new house.</p>
<p>The local authorities in Long An contributed another 8 million dong to build the house.</p>
<p>Now, the family has a porch, solid brick walls surrounding them and a stable roof. A young puppy hides behind a dusty motor in front of the facade.</p>
<p>All the house is missing is a coat of paint.</p>
<p>On the backs of motorbikes, UCLA student Steven Pham and UC Irvine student Tran Huynh follow Ngan&rsquo;s typical route to school.</p>
<p>While Ngan and her father were at the MEMO scholarship ceremony, mother Hoa Kim Thi Huynh tells the students that until recently, the family was making ends meet by picking lemons for $1, or 20,000 dong a day.</p>
<p>When the price of lemons plummeted, they couldn&rsquo;t pick enough of the citrus for their boss to make a profit, and they were fired.</p>
<p>Ngan&rsquo;s $60 MEMO scholarship is the same amount it would cost the family to replace their expired mosquito nets, the students learn.</p>
<p>At the side of the house, a pot of rice boils in an outdoor kitchen, detached from the house.</p>
<p>Grandmother Sua Thi Huynh, going blind, works to string tiny yellow beads on a metal wire. She is making a curtain that eventually sells for $4 after two days of tedious work.</p>
<p>But even after a long day of school or work, the family has a house together with a reminder of how it got there. Above the doorway, the sign reads “<i>Nh&agrave; T&igrave;nh Thuong</i>&rdquo;&mdash;a house of love.</p>
